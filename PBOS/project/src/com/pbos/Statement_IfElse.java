package com.pbos;

public class Statement_IfElse {
    public static void main(String[] args) {
        int x = 10;
        if (x < 5 ) {
            System.out.println("Nilai x lebih besar dari 5");
        }
        else {
            System.out.println("Nilai x lebih kecil dari 5");
        }
    }
    
}
